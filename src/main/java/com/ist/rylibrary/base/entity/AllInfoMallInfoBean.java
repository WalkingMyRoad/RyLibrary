public class AllInfoMallInfoBean {
    /**电话*/
    private String phone;
    /**联系地址*/
    private String address;
    /**区域编码（街道）*/
    private String areaCode;
    /**联系人*/
    private String contactName;
    /**网点编号*/
    private String mallNo;
    /**网点ID*/
    private String mallId;
    /**网点名称*/
    private String mallName;

    public AllInfoMallInfoBean() {
    }

    public AllInfoMallInfoBean(String phone, String address, String areaCode, String contactName, String mallNo, String mallId, String mallName) {
        this.phone = phone;
        this.address = address;
        this.areaCode = areaCode;
        this.contactName = contactName;
        this.mallNo = mallNo;
        this.mallId = mallId;
        this.mallName = mallName;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public String getContactName() {
        return contactName;
    }

    public void setContactName(String contactName) {
        this.contactName = contactName;
    }

    public String getMallNo() {
        return mallNo;
    }

    public void setMallNo(String mallNo) {
        this.mallNo = mallNo;
    }

    public String getMallId() {
        return mallId;
    }

    public void setMallId(String mallId) {
        this.mallId = mallId;
    }

    public String getMallName() {
        return mallName;
    }

    public void setMallName(String mallName) {
        this.mallName = mallName;
    }

    @Override
    public String toString() {
        return "AllInfoMallInfoBean{" +
                "phone='" + phone + '\'' +
                ", address='" + address + '\'' +
                ", areaCode='" + areaCode + '\'' +
                ", contactName='" + contactName + '\'' +
                ", mallNo='" + mallNo + '\'' +
                ", mallId='" + mallId + '\'' +
                ", mallName='" + mallName + '\'' +
                '}';
    }
}
