public class FinalQAnswerBean {
    private String type;
    private String text;

    public FinalQAnswerBean() {
    }

    public FinalQAnswerBean(String type, String text) {
        this.type = type;
        this.text = text;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    @Override
    public String toString() {
        return "FinalQAnswerBean{" +
                "type='" + type + '\'' +
                ", text='" + text + '\'' +
                '}';
    }
}
