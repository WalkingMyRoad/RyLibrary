public class FinalQASemanticSlotsBean {
    private FinalQASemanticSlotsObjBean obj;
    private String oper;
    /**手机号码*/
    private String code;
    /**索引修改位置*/
    private String posRank;
    /**修改的号码内容*/
    private String correctPart;
    /**错误的号码修改点*/
    private String wrongPart;
    private FinalQASemanticSlotsObjectBean object;

    public FinalQASemanticSlotsBean() {
    }

    public FinalQASemanticSlotsBean(FinalQASemanticSlotsObjBean obj, String oper, String code, String posRank, String correctPart, String wrongPart, FinalQASemanticSlotsObjectBean object) {
        this.obj = obj;
        this.oper = oper;
        this.code = code;
        this.posRank = posRank;
        this.correctPart = correctPart;
        this.wrongPart = wrongPart;
        this.object = object;
    }

    public String getPosRank() {
        return posRank;
    }

    public void setPosRank(String posRank) {
        this.posRank = posRank;
    }

    public String getCorrectPart() {
        return correctPart;
    }

    public void setCorrectPart(String correctPart) {
        this.correctPart = correctPart;
    }

    public String getWrongPart() {
        return wrongPart;
    }

    public void setWrongPart(String wrongPart) {
        this.wrongPart = wrongPart;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public FinalQASemanticSlotsObjBean getObj() {
        return obj;
    }

    public void setObj(FinalQASemanticSlotsObjBean obj) {
        this.obj = obj;
    }

    public String getOper() {
        return oper;
    }

    public void setOper(String oper) {
        this.oper = oper;
    }

    public FinalQASemanticSlotsObjectBean getObject() {
        return object;
    }

    public void setObject(FinalQASemanticSlotsObjectBean object) {
        this.object = object;
    }


    @Override
    public String toString() {
        return "FinalQASemanticSlotsBean{" +
                "obj=" + obj +
                ", oper='" + oper + '\'' +
                ", code='" + code + '\'' +
                ", posRank='" + posRank + '\'' +
                ", correctPart='" + correctPart + '\'' +
                ", wrongPart='" + wrongPart + '\'' +
                ", object=" + object +
                '}';
    }
}
