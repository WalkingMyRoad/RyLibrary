public class ChassisMessageEvent {
    /**是否是自定义处理*/
    private boolean isCustom;
    /**是否有人*/
    private String message;
    /**底盘解决的类型*/
    private String type;

    public ChassisMessageEvent(boolean isCustom, String message,String type){
        this.isCustom = isCustom;
        this.message = message;
        this.type = type;
    }

    public boolean isCustom() {
        return isCustom;
    }

    public String getMessage() {
        return message;
    }

    public String getType() {
        return type;
    }
}
